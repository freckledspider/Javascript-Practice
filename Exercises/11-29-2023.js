// What are data types in Javascript? What are primitive data types?
// A data type determines the type of values that can be stored in a variable. There are two types of data types in Javascript: primitive and non-primitive.

// Primitive data types: number, string, boolean, undefined, null 
// Primitive data types are immutable, which means that their values cannot be changed or modified once they are created.

